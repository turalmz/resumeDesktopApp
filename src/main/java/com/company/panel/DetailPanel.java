    private void fillWindow() {
        cbBirthplace.removeAllItems();
        cbNationality.removeAllItems();

        for (Country con : countryDao.getAll()) {
            cbBirthplace.addItem(con);
            cbNationality.addItem(con);
        }

    }

    private SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

    public void fillUserComponent() {

        try {
            Date dt = Config.loggedInUser.getBirthDate();
            String sdt = sdf.format(dt);
            txtBirthdate.setText(sdt);
        } catch (Exception ex) {
            ex.fillInStackTrace();
        }
        txtEmail.setText(Config.loggedInUser.getEmail());
        txtPhone.setText(Config.loggedInUser.getPhone());
        txtAddress.setText(Config.loggedInUser.getAddress());
        cbBirthplace.setSelectedItem(Config.loggedInUser.getBirthPlace());
        cbNationality.setSelectedItem(Config.loggedInUser.getNationality());

    }
