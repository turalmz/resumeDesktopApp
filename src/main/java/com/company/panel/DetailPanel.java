    public void fillUser(User usr) {
        try {
            long l = Config.sdf.parse(txtBirthdate.getText()).getTime();
            Date bd = new Date(l);
            usr.setBirthDate(bd);

        } catch (ParseException ex) {
            System.out.print("Houston, we have a problem");
        }

        usr.setAddress(txtAddress.getText());
        usr.setPhone(txtPhone.getText());
        usr.setEmail(txtEmail.getText());
    }
